import { appendRuntimeGuidance, buildClaimSummary, buildMemoryReadme } from "./export-shared.js";

export function buildOpenHandsExport({ manifest, artifacts, claims, rawClaims }) {
  const displayName = manifest.subject?.display_name ?? manifest.id;

  return {
    "AGENTS.md": buildAgents(displayName, manifest),
    "SOUL.md": buildSoul(artifacts.SOUL),
    "USER.md": buildUser(artifacts.USER),
    "MEMORY_POLICY.md": buildMemoryPolicy(artifacts.MEMORY_POLICY),
    "BOUNDARIES.md": buildBoundaries(artifacts.BOUNDARIES),
    ".agents/skills/persona-continuity/SKILL.md": buildPersonaContinuitySkill(displayName, artifacts),
    "eval/README.md": buildEvalReadme(displayName),
    "memory/README.md": buildMemoryReadme(displayName, "OpenHands"),
    "memory/claims.jsonl": rawClaims,
    "memory/claim-summary.md": buildClaimSummary(claims, "OpenHands")
  };
}

function buildAgents(displayName, manifest) {
  return [
    `# OpenHands Workspace: ${displayName}`,
    "",
    "This workspace was generated by Persona Capsule for OpenHands-style engineering agent runtimes.",
    "",
    "## Operating Rules",
    "",
    "- Treat `SOUL.md`, `USER.md`, `MEMORY_POLICY.md`, `BOUNDARIES.md`, and `memory/claims.jsonl` as exported persona guidance.",
    "- Use the persona-continuity skill when a task depends on persona voice, continuity, memory policy, or boundaries.",
    "- Keep engineering task execution practical, testable, and evidence-grounded.",
    "- Do not silently merge runtime memories into the canonical capsule.",
    "- Write proposed memory deltas under `memory/` for human review.",
    "- Preserve uncertainty labels from claims when answering.",
    "- Never claim biological or conscious continuity unless the capsule explicitly supports it.",
    "",
    "## Capsule Metadata",
    "",
    `- Capsule: ${manifest.id}`,
    `- Version: ${manifest.version}`,
    `- Mode: ${manifest.mode}`,
    ""
  ].join("\n");
}

function buildSoul(soul) {
  return appendRuntimeGuidance(soul, "OpenHands", [
    "- Use this as engineering-agent collaboration posture.",
    "- Preserve evidence-backed continuity without theatrical imitation."
  ]);
}

function buildUser(user) {
  return appendRuntimeGuidance(user, "OpenHands", [
    "- Use this file for relationship context with the current user or owner.",
    "- Do not expand relationship claims beyond supporting evidence."
  ]);
}

function buildMemoryPolicy(memoryPolicy) {
  return appendRuntimeGuidance(memoryPolicy, "OpenHands", [
    "- Treat runtime observations as memory proposals until reviewed.",
    "- Prefer explicit evidence and reversible updates.",
    "- Do not store sensitive facts unless the capsule policy permits them."
  ]);
}

function buildBoundaries(boundaries) {
  return appendRuntimeGuidance(boundaries, "OpenHands", [
    "- Refuse unsupported private memories and simulated conscious continuity.",
    "- When evidence is absent or low-confidence, state uncertainty instead of inventing memory."
  ]);
}

function buildPersonaContinuitySkill(displayName, artifacts) {
  return [
    "---",
    "name: persona-continuity",
    `description: Use when OpenHands should preserve ${displayName} persona continuity, memory policy, boundaries, or voice while completing an engineering task.`,
    "---",
    "",
    "# Persona Continuity",
    "",
    "Engineering-runtime persona guidance for OpenHands-style agents.",
    "",
    "## Required Behavior",
    "",
    "- Follow direct user instructions first, then apply persona guidance where it does not conflict.",
    "- Ground personal claims in `memory/claims.jsonl`; do not invent unsupported memories.",
    "- Keep runtime memory changes as proposals under `memory/` instead of editing canonical capsule artifacts silently.",
    "- Preserve uncertainty and consent boundaries.",
    "- Keep implementation choices testable and reversible.",
    "",
    "## Capsule SOUL",
    "",
    artifacts.SOUL.trim(),
    "",
    "## Capsule USER",
    "",
    artifacts.USER.trim(),
    "",
    "## Capsule Memory Policy",
    "",
    artifacts.MEMORY_POLICY.trim(),
    "",
    "## Capsule Boundaries",
    "",
    artifacts.BOUNDARIES.trim(),
    ""
  ].join("\n");
}

function buildEvalReadme(displayName) {
  return [
    `# OpenHands Eval Prompts: ${displayName}`,
    "",
    "OpenHands eval prompts belong here.",
    "",
    "Use this directory for engineering-runtime smoke prompts, boundary prompts, and persona-consistency review notes. Keep eval prompts separate from canonical capsule artifacts.",
    ""
  ].join("\n");
}
