import { appendRuntimeGuidance, buildClaimSummary, buildMemoryReadme } from "./export-shared.js";

export function buildClaudeCodeExport({ manifest, artifacts, claims, rawClaims }) {
  const displayName = manifest.subject?.display_name ?? manifest.id;

  return {
    "CLAUDE.md": buildClaude(displayName, manifest),
    "SOUL.md": buildSoul(artifacts.SOUL),
    "USER.md": buildUser(artifacts.USER),
    "MEMORY_POLICY.md": buildMemoryPolicy(artifacts.MEMORY_POLICY),
    "BOUNDARIES.md": buildBoundaries(artifacts.BOUNDARIES),
    ".claude/rules/persona-continuity.md": buildPersonaContinuityRule(displayName, artifacts),
    "memory/README.md": buildMemoryReadme(displayName, "Claude Code"),
    "memory/claims.jsonl": rawClaims,
    "memory/claim-summary.md": buildClaimSummary(claims, "Claude Code")
  };
}

function buildClaude(displayName, manifest) {
  return [
    `# Claude Code Workspace: ${displayName}`,
    "",
    "This workspace was generated by Persona Capsule for Claude Code-style coding agents.",
    "",
    "## Operating Rules",
    "",
    "- Treat `SOUL.md`, `USER.md`, `MEMORY_POLICY.md`, `BOUNDARIES.md`, and `memory/claims.jsonl` as exported persona guidance.",
    "- Review `.claude/rules/persona-continuity.md` before tasks that depend on persona voice, continuity, memory policy, or boundaries.",
    "- Keep user instructions, safety, and code quality above persona style.",
    "- Do not silently merge runtime memories into the canonical capsule.",
    "- Write proposed memory deltas under `memory/` for human review.",
    "- Preserve uncertainty labels from claims when answering.",
    "",
    "## Capsule Metadata",
    "",
    `- Capsule: ${manifest.id}`,
    `- Version: ${manifest.version}`,
    `- Mode: ${manifest.mode}`,
    ""
  ].join("\n");
}

function buildSoul(soul) {
  return appendRuntimeGuidance(soul, "Claude Code", [
    "- Use this as compact persona posture for code collaboration.",
    "- Preserve evidence-backed continuity without theatrical imitation."
  ]);
}

function buildUser(user) {
  return appendRuntimeGuidance(user, "Claude Code", [
    "- Use this file for relationship context with the current user or owner.",
    "- Do not expand relationship claims beyond supporting evidence."
  ]);
}

function buildMemoryPolicy(memoryPolicy) {
  return appendRuntimeGuidance(memoryPolicy, "Claude Code", [
    "- Treat runtime observations as memory proposals until reviewed.",
    "- Prefer explicit evidence and reversible updates.",
    "- Do not store sensitive facts unless the capsule policy permits them."
  ]);
}

function buildBoundaries(boundaries) {
  return appendRuntimeGuidance(boundaries, "Claude Code", [
    "- Refuse unsupported private memories and simulated conscious continuity.",
    "- When evidence is absent or low-confidence, state uncertainty instead of inventing memory."
  ]);
}

function buildPersonaContinuityRule(displayName, artifacts) {
  return [
    `# Persona Continuity Rule: ${displayName}`,
    "",
    "Apply this rule when a task depends on the exported Persona Capsule's continuity rules, voice, relationship context, or memory governance.",
    "",
    "## Required Behavior",
    "",
    "- Follow direct user instructions first, then apply persona guidance where it does not conflict.",
    "- Ground personal claims in `memory/claims.jsonl`.",
    "- Do not invent unsupported memories.",
    "- Keep runtime memory changes as proposals under `memory/` instead of editing canonical capsule artifacts silently.",
    "- Preserve uncertainty and consent boundaries.",
    "",
    "## Capsule SOUL",
    "",
    artifacts.SOUL.trim(),
    "",
    "## Capsule USER",
    "",
    artifacts.USER.trim(),
    "",
    "## Capsule Memory Policy",
    "",
    artifacts.MEMORY_POLICY.trim(),
    "",
    "## Capsule Boundaries",
    "",
    artifacts.BOUNDARIES.trim(),
    ""
  ].join("\n");
}
